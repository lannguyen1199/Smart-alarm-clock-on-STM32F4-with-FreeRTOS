// 3. CÁC HÀM CHO FONT LỚN 12x16 (Dùng 2 Pages)
// ============================================================================

// Đã bỏ tham số *fond truyền vào, tự động lấy fond12x16 nội bộ
void poit_1(int page, int column, char ky_tu) {
    if (ky_tu == ' ') {
        pos(page, column);
        for(int i = 0; i < 12; i++) I2C_write_dulieu(0x00);
        pos(page + 1, column);
        for(int i = 0; i < 12; i++) I2C_write_dulieu(0x00);
        return;
    }

    uint16_t index = 0;
    if (ky_tu >= '0' && ky_tu <= '9') {
        index = (ky_tu - '0') * 12;  // mỗi ký tự có 12 cột
    }
    else if (ky_tu >= 'A' && ky_tu <= 'Z') {
        index = ((ky_tu - '0') - 7) * 12;
    }
    else if (ky_tu == '.') {
        index = (('0' - ky_tu) + 34) * 12;
    }
    else if (ky_tu == ':') {
        index = (('0' - '.') + 34) * 12; // Tạm mượn dấu chấm cho dấu 2 chấm
    }
    else return;

    // PAGE TRÊN (8 dòng đầu)
    pos(page, column);
    for(int i = 0; i < 12; i++) {
        uint16_t data = fond12x16[index + i];
        I2C_write_dulieu(data & 0x00FF);
    }

    // PAGE DƯỚI (8 dòng sau)
    pos(page + 1, column);
    for(int i = 0; i < 12; i++) {
        uint16_t data = fond12x16[index + i];
        I2C_write_dulieu(data >> 8);      // byte cao
    }
}

// Đã bỏ tham số *fond
void point_many(int page, int column, char* str) {
    while (*str) {
        poit_1(page, column, *str);
        column += 12;    // mỗi ký tự rộng 12 cột
        str++;
    }
}

// ============================================================================
// 4. CÁC HÀM CHO FONT NHỎ 5x7 (Dùng 1 Page)
// ============================================================================

void oled_write_5x7(int page, int column, char ky_tu) {
    // Nếu là ký tự không in được hoặc ngoài bảng, đưa về Space
    if (ky_tu < 32 || ky_tu > 126) ky_tu = ' ';

    // Tính toán index trong mảng 1 chiều (mỗi chữ chiếm 5 byte)
    uint16_t index = (ky_tu - 32) * 5;

    pos(page, column);
    for(int i = 0; i < 5; i++) {
        I2C_write_dulieu(font5x7[index + i]);
    }
    // Cột khoảng cách giữa các chữ
    I2C_write_dulieu(0x00);
}

void oled_string_5x7(int page, int column, char* str) {
    while (*str) {
        oled_write_5x7(page, column, *str);
        column += 6; // 5 cột font + 1 cột khoảng cách trắng
        str++;

        // Nếu tràn viền màn hình (128 cột), tự động ngắt
        if(column > 122) break;
    }
}
