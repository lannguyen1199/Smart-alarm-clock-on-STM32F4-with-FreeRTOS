void Clear_all(void) {
    for (int page = 0; page < 8; page++) {
        I2C_write_lenh(0xB0 + page);
        I2C_write_lenh(0x00);
        I2C_write_lenh(0x10);
        for (int i = 0; i < 130; i++) {
            I2C_write_dulieu(0x00);
        }
    }
}

void Clear_page(void) {
    // Chỉ xóa phần dưới (Page 4 đến 7) để giữ lại đồng hồ phía trên
    for (int page = 4; page < 8; page++) {
        I2C_write_lenh(0xB0 + page);
        I2C_write_lenh(0x00);
        I2C_write_lenh(0x10);
        for (int i = 0; i < 130; i++) {
            I2C_write_dulieu(0x00);
        }
    }
}

void pos(int page, int column) {
    I2C_write_lenh(0xB0 + page);
    I2C_write_lenh(0x10 | (column >> 4));
    I2C_write_lenh(column & 0x0f);
}

