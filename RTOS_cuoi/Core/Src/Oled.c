void I2C_write_lenh(uint8_t lenh) {
    uint8_t ma_lenh[2] = {0x00, lenh};
    gui_du_lieu(AdOled, ma_lenh, 2);
}

void I2C_write_dulieu(uint8_t du_lieu) {
    uint8_t ma_du_lieu[2] = {0x40, du_lieu};
    gui_du_lieu(AdOled, ma_du_lieu, 2);
}

